# 7. **Area of a Trapezium**
# Input parallel sides a, b and height h.

a= float(input("Enter length of first parallel side of trapezium: "))
b= float(input("Enter length of second parallel side of trapezium: "))
h= float(input("Enter height of trapezium: "))
area= ((a + b) / 2) * h
print("Area of Trapezium: ", area)